// Scroll reveal
const reveals = document.querySelectorAll(".reveal");
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        setTimeout(() => {
          entry.target.classList.add("visible");
        }, 80);
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12 },
);
reveals.forEach((el) => observer.observe(el));

// Form submit → redirect to WA
function handleSubmit(e) {
  e.preventDefault();
  const form = e.target;
  const nama = form.querySelector('input[type="text"]').value;
  const wa = form.querySelector('input[type="tel"]').value;
  const layanan = form.querySelector("select").value;
  const pesan = form.querySelector("textarea").value;
  const greeting =
    currentLang === "en"
      ? `Hello PT. Dayasa Cipta Mandiri,%0A%0AName: ${encodeURIComponent(nama)}%0AWhatsApp: ${encodeURIComponent(wa)}%0AService: ${encodeURIComponent(layanan)}%0A%0AMessage:%0A${encodeURIComponent(pesan)}`
      : `Halo PT. Dayasa Cipta Mandiri,%0A%0ANama: ${encodeURIComponent(nama)}%0ANo. WA: ${encodeURIComponent(wa)}%0ALayanan: ${encodeURIComponent(layanan)}%0A%0APesan:%0A${encodeURIComponent(pesan)}`;
  window.open(`https://wa.me/6285791719393?text=${greeting}`, "_blank");
}

(function () {
  const CARDS = [
    { src: "/assets/images/galeri1.jpeg" },
    { src: "/assets/images/galeri2.jpeg" },
    { src: "/assets/images/galeri3.jpeg" },
    { src: "/assets/images/galeri4.jpeg" },
    { src: "/assets/images/galeri5.jpeg" },
    { src: "/assets/images/galeri6.jpeg" },
  ];

  const track = document.getElementById("galTrack");
  const nav = document.getElementById("galNav");
  const slider = document.getElementById("galSlider");

  track.innerHTML = "";
  track.style.animation = "none";

  function makeCard(card) {
    const div = document.createElement("div");
    div.className = "gallery-card";
    const img = document.createElement("img");
    img.src = card.src;
    img.alt = "";
    img.loading = "lazy";
    div.appendChild(img);
    div.insertAdjacentHTML("beforeend", `<div class="card-overlay"></div>`);
    return div;
  }

  /* 3 set agar loop seamless */
  [...CARDS, ...CARDS, ...CARDS].forEach((c) => track.appendChild(makeCard(c)));

  let setWidth = 0;
  let cardW = 0;
  let currentX = 0;
  const speed = 0.6;
  let isPaused = false;
  let isDragging = false;
  let started = false;

  function calcDimensions() {
    const firstCard = track.querySelector(".gallery-card");
    if (!firstCard) return;
    const gap = parseFloat(getComputedStyle(track).gap) || 20;
    cardW = firstCard.offsetWidth + gap;
    setWidth = cardW * CARDS.length;
    if (!started) {
      currentX = -setWidth; /* mulai di set B (tengah) */
      track.style.transform = `translateX(${currentX}px)`;
      started = true;
    }
  }

  /* ── Update dot visual saja, TIDAK ubah currentX ── */
  function updateDotVisual(idx) {
    document
      .querySelectorAll(".gallery-dot")
      .forEach((d, i) => d.classList.toggle("active", i === idx));
  }

  /* ── Klik dot → lompat ke kartu itu (ubah currentX) ── */
  function jumpToCard(idx) {
    /* posisi kartu idx di set B = -setWidth - (cardW * idx) */
    currentX = -setWidth - cardW * idx;
    updateDotVisual(idx);
  }

  /* ── Render dots ── */
  const TOTAL = CARDS.length;
  CARDS.forEach((_, i) => {
    const btn = document.createElement("button");
    btn.className = "gallery-dot" + (i === 0 ? " active" : "");
    btn.setAttribute("aria-label", `Galeri ${i + 1}`);
    btn.addEventListener("click", () => jumpToCard(i));
    nav.appendChild(btn);
  });

  /* ── Animate loop ── */
  function animate() {
    if (!isPaused && !isDragging) currentX -= speed;

    /* seamless reset */
    if (currentX <= -setWidth * 2) currentX += setWidth;
    if (currentX > 0) currentX -= setWidth;

    track.style.transform = `translateX(${currentX}px)`;

    /* Sync dot visual berdasarkan posisi — tidak ubah currentX */
    if (setWidth > 0 && cardW > 0) {
      const relX = Math.abs(currentX + setWidth); /* relatif ke set B */
      const idx = Math.floor(relX / cardW) % TOTAL;
      updateDotVisual(idx);
    }

    requestAnimationFrame(animate);
  }

  function init() {
    calcDimensions();
    if (setWidth > 0) animate();
  }

  if (document.readyState === "complete") {
    init();
  } else {
    window.addEventListener("load", init);
    setTimeout(() => {
      if (!started) init();
    }, 800);
  }

  /* ── Pause on hover ── */
  slider.addEventListener("mouseenter", () => (isPaused = true));
  slider.addEventListener("mouseleave", () => {
    isPaused = false;
    isDragging = false;
  });

  /* ── Drag (mouse) ── */
  let dragStartX = 0,
    dragStartPos = 0;

  slider.addEventListener("mousedown", (e) => {
    isDragging = true;
    dragStartX = e.pageX;
    dragStartPos = currentX;
    slider.classList.add("is-dragging");
  });
  window.addEventListener("mouseup", () => {
    isDragging = false;
    slider.classList.remove("is-dragging");
  });
  slider.addEventListener("mousemove", (e) => {
    if (!isDragging) return;
    currentX = dragStartPos + (e.pageX - dragStartX);
  });

  /* ── Touch ── */
  let touchStartX = 0,
    touchStartPos = 0;

  slider.addEventListener("touchstart", (e) => {
    touchStartX = e.touches[0].pageX;
    touchStartPos = currentX;
    isDragging = true;
    slider.classList.add("is-dragging");
  });
  slider.addEventListener("touchend", () => {
    isDragging = false;
    slider.classList.remove("is-dragging");
  });
  slider.addEventListener("touchmove", (e) => {
    if (!isDragging) return;
    currentX = touchStartPos + (e.touches[0].pageX - touchStartX);
  });
})();
